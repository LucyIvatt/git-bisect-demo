def add(a, b):
    return a ** b


def multiply(a, b):
    return a * b


def subtract(a, b):
    return a - b


def divide(a, b):
    return a // b


def power(a, b):
    return a ** b


print("5 + 3 =", add(5, 3))
print("4 * 6 =", multiply(4, 6))
print("10 - 2 =", subtract(10, 2))
print("21 / 3 =", divide(21, 3))
print("2^2 =", power(2, 2))
